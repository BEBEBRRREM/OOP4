#!/usr/bin/env python3
# -*- coding: utf-8 -*-

# Запрашиваем ввод двух значений
value1 = input("Введите первое значение: ")
value2 = input("Введите второе значение: ")

# Проверяем, являются ли оба значения числами
try:
    num1 = float(value1)  # Пробуем преобразовать в число
    num2 = float(value2)  # Пробуем преобразовать в число
    result = num1 + num2  # Если оба числа, складываем
    print("Сумма:", result)
except ValueError:
    # Если возникла ошибка, значит хотя бы одно значение не число
    result = value1 + value2  # Конкатенируем строки
    print("Результат конкатенации:", result)